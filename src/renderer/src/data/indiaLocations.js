export const STATES = Object.keys(INDIA_LOCATIONS).sort()

export function getDistricts(state) {
  if (!state || !INDIA_LOCATIONS[state]) return []
  return Object.keys(INDIA_LOCATIONS[state]).sort()
}

export function getCities(state, district) {
  if (!state || !district) return []
  return INDIA_LOCATIONS[state]?.[district] || []
}
